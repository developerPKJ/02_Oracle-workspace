-- 이 상황에서 옆 사람이 급하게 테이블을 하나 만들어달라고 요청!!
-- 테이블 생성 (DDL)
CREATE TABLE TEST (
    TID NUMBER
);
--> Table TEST이(가) 생성되었습니다.
--> DDL 수행 시 자동 커밋됨!!
--  (엄밀히 말하면 COMMIT 을 하고 DDL 구문을 실행시켜줌)

/*
    * DDL 구문을 실행하는 순간 (CREATE, ALTER, DROP)
    기존 트랜잭션에 있던 모든 변경사항들 (DML 구문 실행 결과들) 을
    일단 무조건 먼저 실제 DB 에 반영 시킨 후 (COMMIT)
    그 다음에 내부적으로 DDL 이 수행된다!!
    
    - 즉, DDL 수행 전 변경사항이 있다면 내가 직접 정확히 COMMIT 이든 ROLLBACK 이든 간에 "픽스" 를 시켜놓고
      DDL 을 실행하는 것이 제일 좋다!!
*/

ROLLBACK;
--> 롤백 완료.

SELECT * FROM EMP_01;
--> 나는 커밋한 적이 없는데도 사원 삭제들이 다 반영이 되서
--  총 20명으로 조회되고 있음!!







