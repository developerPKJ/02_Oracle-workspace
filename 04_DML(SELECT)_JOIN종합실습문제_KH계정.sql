-- 7. '한국' 과 '일본' 에 근무하는 직원들의
--    사원명, 부서명, 근무지역명, 근무국가명을 조회하시오
--    단, 이름 기준 가나다순으로 정렬해서 조회
-->> 오라클 전용 구문
SELECT E.EMP_NAME, D.DEPT_TITLE, L.LOCAL_NAME, N.NATIONAL_NAME
FROM EMPLOYEE E, DEPARTMENT D, LOCATION L, NATIONAL N
WHERE E.DEPT_CODE = D.DEPT_ID(+)
AND D.LOCATION_ID = L.LOCAL_CODE(+)
AND L.NATIONAL_CODE = N.NATIONAL_CODE(+)
AND N.NATIONAL_NAME IN ('한국', '일본');
-->> ANSI 구문
SELECT E.EMP_NAME, D.DEPT_TITLE, L.LOCAL_NAME, N.NATIONAL_NAME
FROM EMPLOYEE E
LEFT JOIN DEPARTMENT D ON E.DEPT_CODE = D.DEPT_ID
LEFT JOIN LOCATION L ON D.LOCATION_ID = L.LOCAL_CODE
LEFT JOIN NATIONAL N ON L.NATIONAL_CODE = N.NATIONAL_CODE
WHERE N.NATIONAL_NAME IN ('한국', '일본');


-- 8. 보너스를 받지 않는 직원들 중 직급코드가 J4 또는 J7 인 직원들의
--    사원명, 직급명, 급여를 조회하시오
-->> 오라클 전용 구문
SELECT E.EMP_NAME, J.JOB_NAME, E.SALARY
FROM EMPLOYEE E, JOB J
WHERE E.JOB_CODE = J.JOB_CODE(+)
AND E.BONUS IS NULL
AND E.JOB_CODE IN ('J4', 'J7');
-->> ANSI 구문
SELECT E.EMP_NAME, J.JOB_NAME, E.SALARY
FROM EMPLOYEE E
LEFT JOIN JOB J ON E.JOB_CODE = J.JOB_CODE
WHERE E.BONUS IS NULL
AND E.JOB_CODE IN ('J4', 'J7');


-- 9. 사번, 사원명, 직급명, 급여등급, 구분을 조회하는데
--    이 때, 구분에 해당하는 값은
--    급여등급이 S1, S2 인 경우 '고급'
--    급여등급이 S3, S4 인 경우 '중급'
--    급여등급이 S5, S6 인 경우 '초급' 으로 조회되게 하시오
-->> 오라클 전용 구문
SELECT E.EMP_ID, E.EMP_NAME, J.JOB_NAME, S.SAL_LEVEL,
    CASE
        WHEN S.SAL_LEVEL IN ('S1', 'S2') THEN '고급'
        WHEN S.SAL_LEVEL IN ('S3', 'S4') THEN '중급'
        WHEN S.SAL_LEVEL IN ('S5', 'S6') THEN '초급'
        ELSE '미정'
    END AS 구분
FROM EMPLOYEE E, JOB J, SAL_GRADE S
WHERE E.JOB_CODE = J.JOB_CODE(+)
AND E.SAL_LEVEL = S.SAL_LEVEL(+);
-->> ANSI 구문
SELECT E.EMP_ID, E.EMP_NAME, J.JOB_NAME, S.SAL_LEVEL,
    CASE
        WHEN S.SAL_LEVEL IN ('S1', 'S2') THEN '고급'
        WHEN S.SAL_LEVEL IN ('S3', 'S4') THEN '중급'
        WHEN S.SAL_LEVEL IN ('S5', 'S6') THEN '초급'
        ELSE '미정'
    END AS 구분
FROM EMPLOYEE E
LEFT JOIN JOB J ON E.JOB_CODE = J.JOB_CODE
LEFT JOIN SAL_GRADE S ON E.SAL_LEVEL = S.SAL_LEVEL;


-- 10. 각 부서별 총 급여합을 조회하되
--     이 때, 총 급여합이 1000만원 이상인 부서명, 급여합을 조회하시오
-->> 오라클 전용 구문
SELECT D.DEPT_TITLE, SUM(E.SALARY) AS 총급여합
FROM EMPLOYEE E, DEPARTMENT D
WHERE E.DEPT_CODE = D.DEPT_ID(+)
GROUP BY D.DEPT_TITLE
HAVING SUM(E.SALARY) >= 10000000;
-->> ANSI 구문
SELECT D.DEPT_TITLE, SUM(E.SALARY) AS 총급여합
FROM EMPLOYEE E
JOIN DEPARTMENT D ON E.DEPT_CODE = D.DEPT_ID
GROUP BY D.DEPT_TITLE
HAVING SUM(E.SALARY) >= 10000000;


-- 11. 각 부서별 평균급여를 조회하여 부서명, 평균급여 (정수처리) 로 조회하시오
--     단, 부서배치가 안된 사원들의 평균도 같이 나오게끔 하시오
-->> 오라클 전용 구문
SELECT D.DEPT_TITLE, FLOOR(AVG(E.SALARY)) AS 평균급여
FROM EMPLOYEE E, DEPARTMENT D
WHERE E.DEPT_CODE = D.DEPT_ID(+)
GROUP BY D.DEPT_TITLE;
-->> ANSI 구문
SELECT D.DEPT_TITLE, FLOOR(AVG(E.SALARY)) AS 평균급여
FROM EMPLOYEE E
LEFT JOIN DEPARTMENT D ON E.DEPT_CODE = D.DEPT_ID
GROUP BY D.DEPT_TITLE;

-- 12. 2010년 이후 입사한 '이'씨 성을 가진 사원의 사원 코드, 사원 명, 부서명 조회
-->> 오라클 전용 구문
SELECT E.EMP_ID, E.EMP_NAME, D.DEPT_TITLE
FROM EMPLOYEE E, DEPARTMENT D
WHERE E.DEPT_CODE = D.DEPT_ID(+)
AND EXTRACT(YEAR FROM E.HIRE_DATE) > 2010
AND E.EMP_NAME LIKE '이%';
-->> ANSI 구문
SELECT E.EMP_ID, E.EMP_NAME, D.DEPT_TITLE
FROM EMPLOYEE E
LEFT JOIN DEPARTMENT D ON E.DEPT_CODE = D.DEPT_ID
WHERE EXTRACT(YEAR FROM E.HIRE_DATE) > 2010
AND E.EMP_NAME LIKE '이%';


