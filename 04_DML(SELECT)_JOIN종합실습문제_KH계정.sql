FROM EMPLOYEE E, JOB J, DEPARTMENT D
WHERE E.JOB_CODE = J.JOB_CODE(+)
AND E.DEPT_CODE = D.DEPT_ID(+)
AND D.DEPT_TITLE LIKE '해외영업%';
-->> ANSI 구문
SELECT E.EMP_NAME, J.JOB_NAME, D.DEPT_ID, D.DEPT_TITLE
