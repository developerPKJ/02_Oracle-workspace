FROM EMPLOYEE E, JOB J
WHERE E.JOB_CODE = J.JOB_CODE(+)
AND E.EMP_NAME LIKE '%영%';
-->> ANSI 구문
SELECT E.EMP_ID, E.EMP_NAME, J.JOB_NAME
FROM EMPLOYEE E
LEFT JOIN JOB J ON E.JOB_CODE = J.JOB_CODE
